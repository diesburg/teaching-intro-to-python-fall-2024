import math

print("The pi constant is "+str(math.pi))

#Performing mathematical operations on pi
doublePi = math.pi * 2

print("2 times pi is "+str(doublePi))

#Get the ceiling of pi
piCeil = math.ceil(math.pi)

print("The ceiling of pi is "+str(piCeil))

#Get the floor of pi
piFloor = math.floor(math.pi)

print("The floor of pi is "+str(piFloor))







